    public void loadData(){
        model.getDataVector().removeAllElements();
        
        model.fireTableDataChanged();
        try{
            Connection c = Koneksi.getKoneksi();
            try (Statement s = c.createStatement()) {
                String sql = "SELECT * FROM tb_buku AS b INNER JOIN tb_kategori AS k USING(id_kategori)";
                try (ResultSet r = s.executeQuery(sql)) {
                    while(r.next()){
                        Object[] o = new Object[7];
                        o[0] = r.getString("id_buku");
                        o[1] = r.getString("judul");
                        o[2] = r.getString("pengarang");
                        o[3] = r.getString("penerbit");
                        o[4] = r.getString("stok");
                        o[5] = r.getString("harga");
                        o[6] = r.getString("nama");
                        model.addRow(o);
                    }
                }
            }
        }catch(SQLException e){
            System.out.println("Terjadi Error");
        }
    }
    
    public void fillCombo(){
        Connection c = Koneksi.getKoneksi();
        Statement s;
        try {
            s = c.createStatement();
            String sql = "SELECT * FROM tb_kategori";
            ResultSet rs = s.executeQuery(sql);
            while(rs.next()){
                String nama = rs.getString("nama");
                cmbKategori.addItem(nama);
            }
        } catch (SQLException ex) {
            Logger.getLogger(bukuMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

