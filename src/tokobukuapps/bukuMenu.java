    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String id = txtId.getText();
        String judul = txtJudul.getText();
        String pengarang = txtPengarang.getText();
        String penerbit = txtPenerbit.getText();
        String stok = txtStok.getText();
        String harga = txtHarga.getText();
        String value = cmbKategori.getSelectedItem().toString();
        Connection c = Koneksi.getKoneksi();
        
        try {
            int no = 0;
            Statement s = c.createStatement();
            String sql = "SELECT id_kategori FROM tb_kategori WHERE nama='"+value+"'";
            ResultSet rs = s.executeQuery(sql);
            if(rs.next()){
                no = rs.getInt(1);
            }
            sql = "INSERT INTO `tb_buku`(`id_buku`, `judul`, `pengarang`, `penerbit`, `stok`, `harga`, `id_kategori`) VALUES ('"+id+"','"+judul+"','"+pengarang+"','"+penerbit+"','"+stok+"','"+harga+"','"+no+"')";
            s.executeUpdate(sql);
            txtId.setText("");
            txtJudul.setText("");
            txtPengarang.setText("");
            txtPenerbit.setText("");
            txtStok.setText("");
            txtHarga.setText("");
            JOptionPane.showMessageDialog(null, "Buku Berhasil Ditambahkan!");
        } catch (SQLException ex) {
            Logger.getLogger(bukuMenu.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Buku Gagal Ditambahkan!");
        }
        
        loadData();
    }//GEN-LAST:event_btnTambahActionPerformed

