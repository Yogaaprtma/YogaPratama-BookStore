    private void tableOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableOrderMouseClicked
        // TODO add your handling code here:
        int brs = tableOrder.rowAtPoint(evt.getPoint());
        String no = tableOrder.getValueAt(brs,0).toString();
        txtNo.setText(no);
        Connection c = Koneksi.getKoneksi();
        try {
            Statement s = c.createStatement();
            String sql = "Select * From tb_order Where no_order='"+no+"'";
            ResultSet rs = s.executeQuery(sql);
            if(rs.next()){
                String date = rs.getString("tgl");
                ((JTextField)dcOrder.getDateEditor().getUiComponent()).setText(date);
            }
        } catch (SQLException ex) {
            Logger.getLogger(orderMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tableOrderMouseClicked

