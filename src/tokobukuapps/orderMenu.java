    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String no = txtNo.getText();
        String value = cmbOrder.getSelectedItem().toString();
        String date = ((JTextField)dcOrder.getDateEditor().getUiComponent()).getText();
        Connection c = Koneksi.getKoneksi();
        try {
            int id = 0;
            Statement s = c.createStatement();
            String sql = "SELECT id_supplier FROM tb_supplier WHERE nama='"+value+"'";
            ResultSet rs = s.executeQuery(sql);
            if(rs.next()){
                id = rs.getInt(1);
            }
            sql= "INSERT INTO `tb_order` VALUES ('"+no+"','"+id+"','"+date+"')";
            s.executeUpdate(sql);
            txtNo.setText("");
            ((JTextField)dcOrder.getDateEditor().getUiComponent()).setText("");
        } catch (SQLException ex) {
            Logger.getLogger(orderMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        loadData();
    }//GEN-LAST:event_btnTambahActionPerformed

